    def 地址标准化(self, 地址):
        """
        地址标准化：统一格式，便于匹配
        """
        if not 地址:
            return ""
        
        # 1. 去除空白
        地址 = re.sub(r'\s+', '', 地址)
        
        # 2. 去除括号
        地址 = re.sub(r'[（）()]', '', 地址)
        
        # 3. 统一行政区划
        地址 = 地址.replace('内蒙古自治区', '')
        地址 = re.sub(r'省(.+?市)', r'\1', 地址)  # "广东省深圳市" → "深圳市"
        
        # 4. 统一楼栋单位
        替换映射 = {
            '号幢': '号', '幢号': '号', '号楼': '号', '栋号': '号',
            '栋': '号', '幢': '号',
        }
        for 旧, 新 in 替换映射.items():
            地址 = 地址.replace(旧, 新)
        
        return 地址
    
    def 解析地址成分(self, 地址):
        """
        解析地址成分：省、市、区、详细地址
        """
        地址 = self.地址标准化(地址)
        
        # 提取市
        市匹配 = re.search(r'([\u4e00-\u9fa5]+市)', 地址)
        市 = 市匹配.group(1) if 市匹配 else ""
        
        # 提取区
        区匹配 = re.search(r'([\u4e00-\u9fa5]+区)', 地址)
        区 = 区匹配.group(1) if 区匹配 else ""
        
        # 提取办事处/街道
        办事处匹配 = re.search(r'([\u4e00-\u9fa5]+办事处)', 地址)
        办事处 = 办事处匹配.group(1) if 办事处匹配 else ""
        
        # 提取路
        路匹配 = re.search(r'([\u4e00-\u9fa5]+[路街道])', 地址)
        路 = 路匹配.group(1) if 路匹配 else ""
        
        # 提取门牌号
        号匹配 = re.search(r'(\d+号)', 地址)
        门牌号 = 号匹配.group(1) if 号匹配 else ""
        
        # 提取小区/大厦名
        小区匹配 = re.search(r'([\u4e00-\u9fa5]+(?:小区|大厦|广场|花园|公寓|商厦|综合楼|家园))', 地址)
        小区名 = 小区匹配.group(1) if 小区匹配 else ""
        
        return {
            '市': 市,
            '区': 区,
            '办事处': 办事处,
            '路': 路,
            '门牌号': 门牌号,
            '小区名': 小区名,
            '完整地址': 地址
        }
    
    def 计算地址匹配度(self, 地址1, 地址2):
        """
        计算两个地址的匹配度（0-1）
        基于地址成分的加权匹配
        """
        成分1 = self.解析地址成分(地址1)
        成分2 = self.解析地址成分(地址2)
        
        # 权重
        权重 = {
            '市': 0.1,
            '区': 0.2,
            '办事处': 0.2,
            '路': 0.2,
            '门牌号': 0.15,
            '小区名': 0.15
        }
        
        总分 = 0
        满分 = 0
        
        for 成分名, 分值 in 权重.items():
            满分 += 分值
            v1 = 成分1[成分名]
            v2 = 成分2[成分名]
            
            if v1 and v2:
                if v1 == v2:
                    总分 += 分值
                elif v1 in v2 or v2 in v1:
                    总分 += 分值 * 0.8  # 包含关系给80%分
                else:
                    # 文本相似度
                    相似度 = self.文本相似度(v1, v2)
                    if 相似度 > 0.5:
                        总分 += 分值 * 相似度
        
        return 总分 / 满分 if 满分 > 0 else 0
    
    def 文本相似度(self, s1, s2):
        """计算两个字符串的相似度（基于编辑距离）"""
        if not s1 or not s2:
            return 0
        
        # 简单的字符集相似度
        set1 = set(s1)
        set2 = set(s2)
        交集 = set1 & set2
        并集 = set1 | set2
        
        return len(交集) / len(并集) if 并集 else 0
    
